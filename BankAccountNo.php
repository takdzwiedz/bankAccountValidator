    public function validate($number) {
        
        //Sprawdzam, czy nr konta ma 26 cyfr
        
        if (strlen($number)!=26){
            return 0;
        }
   
        $W = array(1,10,3,30,9,90,27,76,81,34,49,5,50,15,53,45,62,38,89,17,73,51,25,56,75,71,31,19,93,57);
        
        // Dodaję na końcu kod kraju podany numerycznie (PL 2521, bo P - 25, a L - 21) źródło: https://goo.gl/9GU28i
              
        $number .= "2521";
        
        // Do cyfr od 2-iej do ostatniej dołączam na końcu dwie początkowe i mam 
        // 30 cyfrowy nr konta gotowy do mnożenia przez wagi
        
        $number = substr($number,2).substr($number,0,2);
        
        // Przemnażam każdą z cyfr przez wagi i dodaję do siebie 
        
        $Z =0;
        
        for ($i=0;$i<30;$i++) {
            $Z += $number[29-$i] * $W[$i];
        }
        
        // Jeżeli modulo z sumy wag poszczegolnych cyfr jest rowne 1 to nr konta jest prawidłowy
        
        if ($Z % 97 == 1){
                      

            return 1;
            
        } else {
            return 0;
        }
       
        echo '<br>';
        
    }

